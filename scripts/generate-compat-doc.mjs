import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const matrixPath = resolve(root, "docs/compat/nodejs.json");
const outputPath = resolve(root, "docs/compat/nodejs.md");
const packagesOutputPath = resolve(root, "docs/compat/packages.md");
const unsupportedOutputPath = resolve(root, "docs/compat/unsupported.md");
const fixturesPath = resolve(root, "fixtures");

const matrix = JSON.parse(readFileSync(matrixPath, "utf8"));

function countByStatus(rows) {
  return rows.reduce((counts, row) => {
    counts[row.currentStatus] = (counts[row.currentStatus] ?? 0) + 1;
    return counts;
  }, {});
}

function statusSummary(rows) {
  const counts = countByStatus(rows);
  return Object.entries(matrix.statuses)
    .map(([status]) => `\`${status}\`: ${counts[status] ?? 0}`)
    .join(" | ");
}

function sourceList() {
  return matrix.sources
    .map((source) => `- [${source.label}](${source.url})`)
    .join("\n");
}

function table(rows, keyLabel, keyName) {
  const header = `| ${keyLabel} | Target | Current | Owner | Test | Limitation |\n| --- | --- | --- | --- | --- | --- |`;
  const body = rows
    .map((row) => {
      const limitation = row.limitation ? escapePipes(row.limitation) : "";
      return [
        `\`${row[keyName]}\``,
        `\`${row.targetStatus}\``,
        `\`${row.currentStatus}\``,
        row.owner,
        `\`${row.test}\``,
        limitation
      ].join(" | ");
    })
    .map((line) => `| ${line} |`)
    .join("\n");
  return `${header}\n${body}`;
}

function statusLegend() {
  return Object.entries(matrix.statuses)
    .map(([status, description]) => `- \`${status}\`: ${description}`)
    .join("\n");
}

function escapePipes(value) {
  return String(value).replaceAll("|", "\\|");
}

const markdown = `# Node.js Compatibility

This document is generated from [docs/compat/nodejs.json](./nodejs.json). Do not edit the tables by hand; update the JSON matrix and run \`npm run compat:docs\`.

## Sources

${sourceList()}

## Status Legend

${statusLegend()}

See [Unsupported APIs And Runtime Limits](./unsupported.md) for stable OpenContainers error codes and browser/security boundaries.

## Summary

Modules: ${statusSummary(matrix.modules)}

Globals: ${statusSummary(matrix.globals)}

## Core Modules

${table(matrix.modules, "Module", "specifier")}

## Globals

${table(matrix.globals, "Global", "name")}
`;

writeFileSync(outputPath, `${markdown.trimEnd()}\n`);
console.log(`Generated ${outputPath}`);

