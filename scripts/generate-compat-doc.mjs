writeFileSync(unsupportedOutputPath, `${unsupportedMarkdown.trimEnd()}\n`);
console.log(`Generated ${unsupportedOutputPath}`);

function packageFixtureTable() {
  const rows = readdirSync(fixturesPath, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .map((entry) => {
      const fixture = entry.name;
      const metadataPath = join(fixturesPath, fixture, "metadata.json");
      const metadata = JSON.parse(readFileSync(metadataPath, "utf8"));
      return {
        fixture,
        category: metadata.category,
        packages: metadata.packages.map((pkg) => `${pkg.name}@${pkg.version}`).join("<br>") || "-",
        commands: metadata.commands.map((command) => `\`${escapePipes(command)}\``).join("<br>"),
        preview: metadata.previewAssertions ? "yes" : "no",
        permissions: metadata.requiredPermissions.map((permission) => `\`${permission}\``).join("<br>") || "-"
      };
    })
    .sort((a, b) => a.fixture.localeCompare(b.fixture));

  const header = "| Fixture | Category | Packages | Commands | Preview | Permissions |\n| --- | --- | --- | --- | --- | --- |";
  const body = rows
    .map((row) => `| \`${row.fixture}\` | ${escapePipes(row.category)} | ${row.packages} | ${row.commands} | ${row.preview} | ${row.permissions} |`)
    .join("\n");
  return `${header}\n${body}`;
}

function unsupportedModuleTable() {
  const rows = matrix.modules
    .filter((row) => row.unsupported)
    .map((row) => ({
      module: row.specifier,
      status: row.currentStatus,
      code: row.unsupported.code,
      probe: row.unsupported.probe,
      limitation: row.limitation
    }));

  const header = "| Module | Current | Error Code | Probe | Limitation |\n| --- | --- | --- | --- | --- |";
  const body = rows
    .map((row) => [
      `\`${row.module}\``,
      `\`${row.status}\``,
      `\`${row.code}\``,
      `\`${escapePipes(row.probe)}\``,
      escapePipes(row.limitation)
    ].join(" | "))
    .map((line) => `| ${line} |`)
    .join("\n");
  return `${header}\n${body}`;
}

